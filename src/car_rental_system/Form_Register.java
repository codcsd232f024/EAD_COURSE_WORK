    private boolean saveUserToDatabase(String email, String userName, String password, String role) {
        // Database connection details
        String url = "jdbc:mysql://localhost:3306/car_rental_system"; // Change to your DB URL
        String username = "root"; // Change to your DB username
        String dbPassword = "";   // Change to your DB password

        try (Connection connection = DriverManager.getConnection(url, username, dbPassword)) {
            // Insert query
            String sql = "INSERT INTO users (email, username, password, role) VALUES (?, ?,?, ?)";
            PreparedStatement preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, email);
            preparedStatement.setString(2, userName);
            preparedStatement.setString(3, password);
            preparedStatement.setString(4, role);

            // Execute the query
            int rowsInserted = preparedStatement.executeUpdate();
            return rowsInserted > 0; // Return true if insertion was successful

        } catch (SQLException e) {
            // Log the exception (optional)
            JOptionPane.showMessageDialog(this, "Error saving user: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            return false; // Return false if an error occurred
        }
    }

